
#include <stdio.h>

int main()
{ 
    int A = 5;
    char letra = 'A';
    float num1 = 7.5;
    double num2 = 3.4;
    printf("Valor de numero inteiro: %d \n", A );
    printf("Variavel do tipo float: %f\n" , num1 );
    printf("Variavel do tipo char: %c\n" , letra);
    printf("Variavel double: %lf" , num2);
    return 0;
}
